public class AddBean{
    private Integer a;
    private Integer b;

    public AddBean() {
    }

    public AddBean(Integer a, Integer b) {
        this.a = a;
        this.b = b;
    }

    public Integer getA() {
        return this.a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Integer getB() {
        return this.b;
    }

    public void setB(Integer b) {
        this.b = b;
    }

}
